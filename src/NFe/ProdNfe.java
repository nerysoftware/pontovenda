    private void enviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enviarActionPerformed
        if (tabelaProdutos.getRowCount() > 0) {
            try {
                String quant = null;
                DefaultTableModel tabeladet = (DefaultTableModel) NFe.NFeNOVA.tabela.getModel();

                String[] dado = new String[6];

                int linha = tabelaProdutos.getSelectedRow();

                if (linha == -1) {
                    JOptionPane.showMessageDialog(this, "Selecione um Produto.", "Produtos", 0,
                            new ImageIcon(getClass().getResource("/imagens/usuarios/info.png")));
                } else {
                    String cod = tabelaProdutos.getValueAt(linha, 0).toString();
                    String tipo = tabelaProdutos.getValueAt(linha, 1).toString();
                    String nom = tabelaProdutos.getValueAt(linha, 2).toString();
                    String preco = tabelaProdutos.getValueAt(linha, 3).toString();
                    int c = 0;
                    int j = 0;
                    quant = JOptionPane.showInputDialog(this, "Quantidade:", "Produtos", JOptionPane.INFORMATION_MESSAGE);
                    while (!ProdutosSql.isNumber(quant) && quant != null) {
                        quant = JOptionPane.showInputDialog(this, "Somente valores númericos maiores que 0:",
                                "Error", JOptionPane.ERROR_MESSAGE);
                    }
                    if ((quant.equals("")) || (quant.equals("0"))) {
                       JOptionPane.showMessageDialog(this, "Insira um valor maior que 0");
                    } else {
                        for (int i = 0; i < NFeNOVA.tabela.getRowCount(); i++) {
                            Object com = NFeNOVA.tabela.getValueAt(i, 0);
                            Object quant1 = NFeNOVA.tabela.getValueAt(i, 4);
                            if (cod.equals(com)) {
                                j = i;
                                int quantT = Integer.parseInt(quant) + Integer.parseInt((String) quant1);
                                NFeNOVA.tabela.setValueAt(String.valueOf(quantT), i, 4);
                                c++;
                                calcular();
//                                NFeNOVA.recebido.setText("0.00");
//                                NFeNOVA.troco.setText("0.00");
                            }
                        }
                        if (c == 0) {

                            dado[0] = cod;
                            dado[1] = tipo;
                            dado[2] = nom;
                            dado[3] = preco;
                            dado[4] = quant;

                            tabeladet.addRow(dado);

//                            vendas.FrmCaixa.tabela.setModel(tabeladet);
//                            calcular();
//
//                             vendas.FrmCaixa.recebido.setText("0.00");
//                             vendas.FrmCaixa.troco.setText("0.00");
                        }
                    }
                }
            } catch (Exception e) {
            }
        } else {
            JOptionPane.showMessageDialog(this, "Não tem registros.", "Produtos", 0,
                    new ImageIcon(getClass().getResource("/imagens/usuarios/info.png")));
        }
    }//GEN-LAST:event_enviarActionPerformed

