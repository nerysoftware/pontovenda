    private void TxtNumeroKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtNumeroKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            try {
                Connection con =  (Connection) DriverManager.getConnection("jdbc:mysql://localhost/pontovenda","root","acessonery");
                PreparedStatement p = con.prepareStatement(" select * from vendas  WHERE numero_ven = ? ");

                p.setString(1 , TxtNumero.getText());

                ResultSet r = p.executeQuery();

                while (r.next()){
                    TxtNumero.setText(r.getString ("numero_ven"));
                    TxtCliente.setText(r.getString("cliente_vend"));
                    TxtEndereco.setText(r.getString("total_ven"));
                    TxtProduto.setText(r.getString("itens_vend"));

                }
            } catch (SQLException ex) {

                Logger.getLogger(OC.class.getName()).log(Level.SEVERE, null, ex);

            }

        }
    }//GEN-LAST:event_TxtNumeroKeyPressed

