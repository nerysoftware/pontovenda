     public void readJTable(){
        
        DefaultTableModel modelo = (DefaultTableModel) TabelaOC.getModel();
        modelo.setNumRows(0);
        orden_carregamento pdao = new orden_carregamento();
       
        for (OrdenCarregamento p : pdao.read()) {
        
            modelo.addRow(new Object[]{
            p.getId_oc(),
            p.getId_pedido(),
            p.getCliente(),
            p.getEndereco(),
            p.getProduto(),
            p.getStatus()
            
          
            
           
       });                       
    
        }
    }
        public void readJTableForDesc(String desc){
        DefaultTableModel modelo = (DefaultTableModel) TabelaOC.getModel();
        modelo.setNumRows(0);
        orden_carregamento pdao = new orden_carregamento();
       
        for (OrdenCarregamento p : pdao.readForDesc(desc)) {
        
            modelo.addRow(new Object[]{
            p.getId_oc(),
            p.getId_pedido(),
            p.getCliente(),
            p.getEndereco(),
            p.getProduto(),
            p.getStatus()
            
           
       });
    
    
    }
         
     }

